package Assignments;

public class Tempscale {

	public static void main(String[] args) {
		double   c = 0.0;
		double f = 1.8*c + 32;
		double k = c + 273;
		System.out.println(c + " Celsius " + " = " + f + " Fahrenheit ");
		System.out.println(c + " Celsius " + " = " + k + " Kelvin ");
		
		int j = 10 * 4 + 2;

		int kk = 10 + 4 * 2;

		int m = 10 + 4 * 2 % 2;

		int g = 10 / 5 * 6 / 2;
		
		System.out.println(j + " " + kk + " " + m + " " + g);
		
	}
	
	
	
	
}
